    <p style="color: black;" class="mb-4">The candidacy nominations for the Open Source Initiative board of elections closed today, March 7th. The OSI board of directors will renew four of its seats with an open election process among its full individual members and affiliates. This month we will be holding two elections:</p>
    <p style="color: black;">
        ■ The Affiliate organizations will elect two directors
    </p>
